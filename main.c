void dump_bytes(const char *title, uint8_t *b, uint n)
{
   printf("%s:\n", title);
   for (int i = 0; i < n; i++) {
      printf("0x%02x, ", b[i]);
      if (i % 8 == 7) printf("\n");
   }
   if (n % 8) printf("\n");
}

